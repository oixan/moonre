     */
    public function index()
    {
        $country = Countries::all()->pluck('name.common','cca2');

        $i = 0;
        $resultArray = array();
        foreach ($country as $key => $value) {
            $resultArray[$i++] = (object) [
                'code' => $key,
                'value' => $value
            ];
        }

        return  response()->json([
            'country' => $resultArray
        ]);
    }

